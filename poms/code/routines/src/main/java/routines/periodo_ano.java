package routines;

public class periodo_ano {

    public static String RetornaTrimestre(String message) {
    	String trimestre = "";
    	
    	switch (message) {

        	case "enero":
        	case "febrero":
        	case "marzo":
        		trimestre = "1T";
        		break;
        	case "abril":
        	case "mayo":
        	case "junio":
        		trimestre = "2T";
        		break;
        	case "julio":
        	case "agosto":
        	case "septiembre":
        		trimestre = "3T";
        		break;
        	case "octubre":
        	case "noviembre":
        	case "diciembre":
        		trimestre = "4T";
        		break;
    	}
    	
    	return trimestre;
    }
    
    public static String RetornaSemestre(String message) {
    	String semestre = "";
    	
    	switch (message) {

        	case "enero":
        	case "febrero":
        	case "marzo":
        	case "abril":
        	case "mayo":
        	case "junio":
        		semestre = "1S";
        		break;
        	case "julio":
        	case "agosto":
        	case "septiembre":
        	case "octubre":
        	case "noviembre":
        	case "diciembre":
        		semestre = "2S";
        		break;
    	}
    	
    	return semestre;
    }
}
