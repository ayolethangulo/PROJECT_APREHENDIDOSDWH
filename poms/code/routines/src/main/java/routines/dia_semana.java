package routines;

public class dia_semana {

    public static int RetornaDiaSemana(String message) {
    	int dia_sem = 0;
    	switch (message) {

        	case "lunes":
        		dia_sem = 1;
        		break;
        	case "martes":
        		dia_sem = 2;
        		break;
        	case "miércoles":
        		dia_sem = 3;
        		break;
        	case "jueves":
        		dia_sem = 4;
        		break;
        	case "viernes":
        		dia_sem = 5;
        		break;
        	case "sábado":
        		dia_sem = 6;
        		break;
        	case "domingo":
        		dia_sem = 7;
        		break;

    	}
    	
    	return dia_sem;
    }
    
    public static String RetornaFindSemana(String message) {
    	String find ="NO";
    	
    	if(message.equals("sábado") || message.equals("domingo")) {
    		find = "SI";
    	}
    	
    	return find;
    }
}
